     public void setDatos() {
        Object[] datos = new Object[dtmProductos.getColumnCount()];
         dtmProductos.setRowCount(0);
        for (Inventario inventario : inv) {
            datos[0] = inventario.getCodigoBarra();
            datos[1] = inventario.getNombreProducto();
            datos[2] = inventario.getPrecio();
            datos[3] = inventario.getCantidad();
            dtmProductos.addRow(datos);
        }
        tblProductos.setModel(dtmProductos);
    }
     
     public void buscarProductos (String [] arrProd) throws NumberFormatException{
          dtmProductos.setRowCount(0);
         inv = new ArrayList<>();
         boolean  bandera=true;
         for (String arrProd1 : arrProd) {
                String[] textosplit = arrProd1.split(",");
            if (textosplit[1].contains(this.txtNombrePro.getText().toLowerCase())) {
                    String producto = textosplit[1];
                    String valUnit = textosplit[3];
                    String Cant = textosplit[2];
                    String codigo = textosplit[0];
                    bandera=false;
              Inventario Inven = new Inventario(producto, Integer.parseInt(Cant), Long.parseLong(valUnit),Long.parseLong(codigo));
               inv.add(Inven);
                
                }
          } if (bandera) {
             lblVerificarProdu.setText("Producto no encontrado");
         }
     }
     
     public void buscarTodo(String [] arrProd){
         dtmProductos.setRowCount(0);
         inv = new ArrayList<>();
         for (String arrProd1 : arrProd) {
                String[] textosplit = arrProd1.split(",");
                    String producto = textosplit[1];
                    String valUnit = textosplit[3];
                    String Cant = textosplit[2];
                    String codigo = textosplit[0];
            Inventario Inven = new Inventario(producto, Integer.parseInt(Cant), Long.parseLong(valUnit),Long.parseLong(codigo));
            inv.add(Inven);
                
          }
     }
     
     public void nuevaVentana(){
        txtNombrePro.setText("");
        dtmProductos.setRowCount(0);
         inv = new ArrayList<>();
     }
